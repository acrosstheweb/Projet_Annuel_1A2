                <p>
                    En 2019, la célèbre étoile nommée Bételgeuse a mystérieusement commencé à diminuer d'intensité. 
                    Bételgeuse est une étoile supergéante rouge sur son lit de mort dans la constellation d'Orion. 
                    Elle est si énorme que si elle était à la place du soleil, son bord extérieur atteindrait l'orbite de Jupiter. 
                    La supernova de cette étoile est l'un des événements astronomiques les plus attendus au monde.<br>
                    La dernière supernova observée a eu lieu en 1604 avec l’étoile de Kepler située dans la constellation Ophiuchus. 
                    L’explosion de l’étoile était si lumineuse, que les enregistrements et les rapports indiquent qu’elle était visible pendant plus de 3 semaines, de jour comme de nuit. 
                    Par conséquent, s'exciter pour la supernova Bételgeuse semble tout à fait naturel, n'est-ce pas ?  
                </p>
                <p>
                    <img src="m_betelgeuse.jpg" class="float-end __img-articleContent" alt="">
                    Bételgeuse est une étoile semi-variable qui s'éclaire et s'assombrit naturellement sur environ 400 jours. 
                    Cependant, quelque chose d'inattendu s'est produit en décembre 2019 quand une image de l'étoile a été comparée à une image antérieure prise en janvier de la même année. 
                    Ces images ont montré que sa surface était significativement plus sombre à la mi-février. 
                    La luminosité de l'étoile avait chuté à 35 unités et son assombrissement était plus important qu’il ne l’avait jamais été auparavant. 
                    Ce comportement, en effet surprenant, a conduit plusieurs scientifiques à spéculer que Bételgeuse pourrait bientôt subir une supernova. 
                    Cependant, la raison exacte derrière l'atténuation inhabituelle de l'étoile n'était pas connue et a déconcerté les astronomes du monde entier. <br>
                    Ils ont tout de même continué à suivre la luminosité de Bételgeuse en utilisant le très grand télescope de l'ESO. 
                    En avril 2020, l'étoile avait retrouvé sa luminosité apparente normale pendant que les observations étaient effectuées.  
                </p>
                <p>
                    Deux idées circulaient dans le monde de l'astronomie : soit il y avait un grand point froid à sa surface ou bien un nuage de poussière s'était formé juste devant l'étoile vue de la terre. 
                    Après plusieurs mois, les astronomes ont finalement trouvé la raison de l'atténuation de Bételgeuse. 
                    Étonnamment, l'explication s'est avérée être un peu des deux idées proposées. 
                    Les chercheurs ont révélé que l'étoile était partiellement cachée par un nuage de poussière qui a finalement conduit à son obscurcissement, ce qui signifie qu’aucun signe n’indiquait une supernova imminente. 
                    Une question se soulève donc: d'où est apparu ce nuage de poussière et comment a-t-il affecté la luminosité de Bételgeuse?<br>
                    La surface des étoiles comme Bételgeuse change régulièrement lorsque des bulles géantes de gaz se déplacent, se rétractent et se gonflent à l'intérieur de l'étoile. 
                    Quelques temps avant la grande diminution de l'éclat de Bételgeuse, une bulle de gaz géante a été éjectée et s'est éloignée de l'étoile. 
                    Plus tard, en raison d'une baisse de température, un point froid est apparu sur l'étoile. 
                    Cette baisse de température a été suffisante pour que le gaz éjecté se condense en poussière solide. 
                    Pour résumer, initialement le point froid à la surface rendait l'étoile plus sombre, puis la condensation du nuage de poussière a ajouté à la chute rapide de la luminosité de l'étoile.  
                </p>
                <p>
                    Bételgeuse est environ 15 à 20 fois plus massive que le soleil et une étoile de cette taille, à son stade avancé d'évolution, pourrait subir une supernova. 
                    Lorsque l'obscurcissement inattendu s'est produit, il n'était pas insensé de se demander si l'obscurcissement était une indication de la mort imminente de l'étoile. 
                    Il s’est cependant avéré que le coupable était un nuage de poussière et de gaz. <br>
                    Bételgeuse est située à environ 550 années-lumière, ce qui signifie que même la lumière met 550 ans à nous parvenir. 
                    Même si l'étoile implose, nous ne le saurons qu'après 550 ans ou peut-être que la supernova a déjà eu lieu mais que la lumière de cet événement ne nous est pas encore parvenue. 
                    C'est en effet l'un des événements astronomiques les plus attendus et il fournira une multitude d'informations aux astronomes pour comprendre la vie et la mort des étoiles. 
                    Ne vous inquiétez pas, la Terre est à une distance sûre et les radiations électromagnétiques mortelles émises par la supernova n'affecteront pas la vie sur notre planète mais nous offriront des vues spectaculaires pendant plusieurs semaines.
                </p>
                
