                <p>
                    Il existe dans l’univers des milliards d’étoiles. Revenons d’abord sur la définition même de ce qu’on appelle une étoile : une étoile est une boule de gaz brillant composée principalement d’hydrogène et d’hélium, mais aussi de gaz et de poussières, maintenues ensemble par sa propre gravité.
                    Durant toute la durée de son existence, l’étoile traverse plusieurs stades différents. Mais alors, quel est le cycle de vie des étoiles, et quelle est leur destinée ?
                </p>
                <p>
                    Tout commence avec un nuage de gaz et de poussière qu’on appelle une nébuleuse. La force attractive de gravité qui y règne rassemble le gaz et les poussières ensemble pour former ce qu’on appelle une proto-étoile. Toujours avec la force de gravité, de plus en plus de particules entrent en collision les unes entre les autres, rendant la proto-étoile de plus en plus massive et volumineuse, et augmentant d’autant plus sa force de gravité, lui permettant ainsi d’attirer encore plus de gaz et de poussière.
                    Au fur et à mesure de sa formation, la gravité écrase la proto-étoile sur elle-même, la rendant de plus en plus dense, ce qui signifie que les particules qui la composent entrent en collision plus fréquemment, entraînant alors une augmentation de sa température.
                    Lorsque la température et la pression sont suffisamment élevées, les noyaux d'hydrogène commencent à fusionner ensemble pour former des noyaux d’hélium. On appelle ce processus la fusion nucléaire. Cette fusion libère beaucoup d’énergie, ce qui permet de garder le cœur de l’étoile chaude.
                    C’est à ce moment-là que notre proto-étoile devient une étoile de la séquence principale. À ce stade de sa vie, la pression externe causée par toute cette énergie est parfaitement équilibrée par la pression interne dûe à la force de gravité. Cet équilibre permet une longue période stable, pouvant durer pendant plusieurs millions d’années. Notre Soleil par exemple, en est actuellement à ce stade de vie.
                </p>
                <p>
                    <img src="m_vie_etoile.jpg" class="float-end __img-articleContent" alt="">
                    Après un certain temps, l’étoile va commencer à épuiser son stock d'hydrogène, qu’on peut apparenter à l’essence de notre étoile. Sans ce stock, l’étoile n’est plus en mesure de faire de la fusion nucléaire. Ainsi, la pression externe de la force de gravité devient plus importante que la pression interne produite par la fusion nucléaire. Cette rupture de l’équilibre qui régnait jusqu’alors provoque une contraction importante de l’étoile en une petite boule. Cette contraction à lieu jusqu’à ce que l’étoile devienne si dense et chaude, que la fusion nucléaire puisse reprendre à nouveau, lui permettant de s’étendre une nouvelle fois exactement comme au tout début, à un détail près : au lieu de simplement former de l’hélium, la fusion nucléaire va former des éléments plus lourds, incluant tous les éléments jusqu’au fer dans le tableau périodique.
                    La façon dont l’étoile s'étend dépend de la taille initiale de l’étoile. Si c’était une étoile de taille petite ou moyenne, elle se transforme en géante rouge. Si c’était une très grosse étoile, alors elle se transformera en super géante rouge.
                    Ces deux cycles complètent le cycle de vie de l’étoile de plusieurs façons différentes.
                    Après un temps relativement court, la géante rouge devient instable et expulse ses couches externes de gaz et de poussières. Après cela, la géante rouge laisse place à un corps chaud, dense et solide, appelé naine blanche, qui ne fait pas de fusion nucléaire. Ainsi, au fur et à mesure du temps, elle devient de plus en plus froide et noire, se transformant peu à peu en naine noire.
                    La super géante rouge, elle, commence par briller un peu plus à nouveau. Après avoir passé plusieurs stade de compression et d’expansion, elle se transforme en supernova, qui forme des éléments encore plus lourds que le fer et qui les éjectent dans l’univers.
                    Ensuite, tout dépend une fois de plus de la taille de l’étoile initiale.
                    Si c’était juste une grosse étoile, alors la supernova donne lieu à un corps dense, appelé étoile à neutron. Mais si l’étoile était extrêmement massive, alors la supernova donne lieu à un trou noir.
                </p>
                <p>
	                Le cycle de vie d’une étoile peut ainsi durer des millions, voire des milliards d’années. Les premières étapes de ce cycle sont similaires pour toutes les étoiles, mais la suite de leur vie dépend de ces premières étapes. Elles peuvent donc se transformer en simple naine noire, ou alors laisser place à un spectacle plus impressionnant comme une supernova, créant ensuite un trou noir, ou une étoile à neutron.
                </p>
                
