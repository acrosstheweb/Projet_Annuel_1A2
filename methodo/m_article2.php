                <p>
                    En 1705, Edmond Halley, célèbre astronome, prédit le retour d’une comète dans le ciel nocturne de la Terre pour le Noël 1758.
                    À juste titre, cette comète porte depuis son nom : la comète de Halley.
                </p>
                <p>
                    <img src="m_halley.jpg" class="float-end __img-articleContent" alt="">
                    Une comète est un résidu du système solaire. Elle est constituée d’un noyau formé de roche, de poussière, de neige et de gaz. Les comètes orbitent autour du Soleil dans la ceinture de Kuiper, située bien au-delà de l’orbite de Pluton située à près de 40 UA (1 UA étant égale à la distance Terre / Soleil, c'est-à-dire 149 500 000 km). <br>
                    Il suffit d’une simple petite perturbation pour que la comète sorte de cette ceinture et se dirige vers le Soleil. Ces comètes ont alors une orbite elliptique très allongée dont une des extrémités passe très près du Soleil.<br>
                    Tant qu’elle est loin de ce dernier, la comète n’est pas lumineuse et n’a pas de queue. Mais en s’approchant, les particules de vent solaire agissent sur sa tête faisant fondre la matière. Un halo lumineux, appelé chevelure de la comète, se déploie alors autour de la comète.
                    On peut aussi voir une queue se déployer. Généralement, une comète possède deux queues (bien qu’elles peuvent en avoir bien plus) : une première provoquée par les vents solaires, appelée la queue ionisée, et une deuxième provoquée par son déplacement, constituée principalement de poussière.
                </p>
                <p>
                    Aujourd’hui, pour le commun des mortels, une comète est simplement un événement agréable à observer. Mais il y a encore quelques centaines d’années, on ne savait pas ce qu’était une comète ! Et comme l’inconnu était source d’angoisse, la population de l’époque voyait en ces comètes des présages en tout genre. On trouve par exemple la trace de passage de comètes comme celle de 451, qui présageait la défaite d’Attila.<br>
                    En 1664, Edmond, alors âgé de 8 ans, voit une comète dans le ciel et… s’ensuit une épidémie de peste noire à Londres. L’année suivante, il en aperçoit une nouvelle et… un incendie ravage la ville de Londres. Il commence alors à être fasciné par les comètes, et le ciel de manière générale.<br>
                    Son père, marchand de savon ayant fait fortune à la suite de la peste noire de 1664, n’hésite pas à lui payer les meilleures écoles.<br>
                    En 1679, âgé de 19 ans, il écrit avec un culot incroyable une lettre au plus grand astronome d’Angleterre lui disant qu’il s’était trompé dans les tables officielles des positions de Saturne et de Jupiter et lui donne les bonnes valeurs. Coup de culot qui fonctionne, puisque cet astronome le pistonne directement auprès de la Royal Society !
                </p>
                <p>
                    Quelques années plus tard, il deviendra un proche ami de d’Isaac Newton. Avec ce dernier, ils arrivent à la conclusion que les comètes, comme les planètes du système solaire, doivent avoir une orbite autour du Soleil. Mais qui dit orbite dit que la comète revient régulièrement se joindre au ciel nocturne de la Terre.
                    Ce ne sont pas les premiers à formuler cette hypothèse, mais ce sont les premiers à l’avoir démontré par le calcul. Halley à alors une idée, une idée qui va le rendre célèbre, sans qu’il ne le sache alors. <br>
                    Plus tôt en 1682, il observe une comète avec l’astronome royal de l’époque, mais à ce moment-là il n’y prête pas une réelle attention.
                    Il se contente de quelques relevés sur un carnet de notes, et c’est tout.
                    Mais en analysant de vieux écrits, il constate qu’on a pu observer d’autres comètes similaires (NDLR : même position dans le ciel, mêmes caractéristiques) en 1607, en 1531 et en 1456, soit une comète tous les 76 ans environ.
                    C’est exactement l’idée qu’on peut se faire d’un objet en orbite qui revient régulièrement… Il publie donc ses travaux en 1705, en prédisant que la comète reviendrait pour Noël de 1758.
                    Sauf qu’à ce moment-là… peu de gens y prêtent attention. Mais en 1757, un an avant l’arrivée prédite par Halley, trois astronomes français décident de déterrer cette prédiction et refont tous les calculs.
                    Ils arrivent alors à la conclusion que la comète, si elle doit repasser, passera au plus près du Soleil à la mi-avril 1959. Ce papier réveille alors l’intérêt de tous les astronomes pour la prédiction d'Edmond, et tous scrutent le ciel au 25 décembre 1758.
                    Miracle ! La comète apparaît exactement là où Edmond Halley l’avait prédit plus de 50 ans plus tôt.
                </p>
                <p>
                Dès le lendemain, le nom de Halley est sur toutes les bouches, non seulement celles des astronomes, mais aussi celles des mondains. Il est alors décidé de donner son nom à la comète, connue aujourd’hui sous le nom de “comète de Halley”.
                </p>
                
